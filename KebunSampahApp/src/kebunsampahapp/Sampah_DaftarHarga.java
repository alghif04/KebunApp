    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) TabelDaftarHarga.getModel();
        model.setRowCount(0); // Clear existing rows
        
        try (Connection connection = DatabaseConnection.getConnection()) {
            String query = "SELECT Jenis, Harga, Satuan FROM Harga";
            try (PreparedStatement statement = connection.prepareStatement(query);
                 ResultSet resultSet = statement.executeQuery()) {
                
                while (resultSet.next()) {
                    String jenis = resultSet.getString("Jenis");
                    double harga = resultSet.getDouble("Harga");
                    String satuan = resultSet.getString("Satuan");
                    
                    model.addRow(new Object[]{jenis, harga, satuan});
                }
            }
        } catch (SQLException e) {
            System.out.println("Terjadi kesalahan saat mengambil data:");
            e.printStackTrace();
        }
    }
    
