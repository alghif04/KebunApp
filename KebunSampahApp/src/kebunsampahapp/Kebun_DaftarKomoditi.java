    private void AddKomoditiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddKomoditiButtonActionPerformed
String nama = tfNama.getText();
String hargaText = tfHarga.getText();
String satuan = dropdownSatuan.getSelectedItem().toString();

System.out.println("Nama: " + nama);
System.out.println("Harga Text: " + hargaText);
System.out.println("Satuan: " + satuan);

// Validate input fields
if (nama.isEmpty() || hargaText.isEmpty() || satuan.isEmpty()) {
    JOptionPane.showMessageDialog(this, "Please fill in all fields.", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

double harga;
try {
    harga = Double.parseDouble(hargaText);
    System.out.println("Parsed Harga: " + harga);
} catch (NumberFormatException e) {
    JOptionPane.showMessageDialog(this, "Invalid harga format. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
    return;
}

// Create a new Komoditi object
Komoditi newKomoditi = new Komoditi(nama, harga, satuan);

System.out.println("New Komoditi: " + newKomoditi);

// Add the new Komoditi to the database
boolean success = komoditiDAO.addKomoditi(newKomoditi);

if (success) {
    populateTable(); // Refresh the table to show the new Komoditi
} else {
    JOptionPane.showMessageDialog(this, "Failed to add Komoditi.", "Error", JOptionPane.ERROR_MESSAGE);
}

    }//GEN-LAST:event_AddKomoditiButtonActionPerformed

    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
// Get the selected row index
int selectedRow = TabelKomoditi.getSelectedRow();

// Ensure a row is selected
if (selectedRow >= 0) {
    // Get the komoditi details from the selected row
    String komoditiNama = (String) TabelKomoditi.getValueAt(selectedRow, 0); // Assuming name is in the first column
    double komoditiHarga = (double) TabelKomoditi.getValueAt(selectedRow, 1); // Assuming harga is in the second column
    String komoditiSatuan = (String) TabelKomoditi.getValueAt(selectedRow, 2); // Assuming satuan is in the third column

    // Confirm the deletion
    int confirm = JOptionPane.showConfirmDialog(this, "Ingin menghapus data?", "Confirm Delete", JOptionPane.YES_NO_OPTION);
    if (confirm == JOptionPane.YES_OPTION) {
        // Delete the komoditi from the database
        boolean success = komoditiDAO.deleteKomoditiByDetails(komoditiNama, komoditiHarga, komoditiSatuan);

        // If deletion is successful, remove the row from the table
        if (success) {
            DefaultTableModel model = (DefaultTableModel) TabelKomoditi.getModel();
            model.removeRow(selectedRow);
            JOptionPane.showMessageDialog(this, "Komoditi berhasil dihapus.");
        } else {
            JOptionPane.showMessageDialog(this, "Failed to delete komoditi.");
        }
    }
} else {
    JOptionPane.showMessageDialog(this, "Please select a row to delete.");
}
    }//GEN-LAST:event_DeleteButtonActionPerformed

