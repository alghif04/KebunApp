    private void AddSetoranButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddSetoranButtonActionPerformed

        java.util.Date utilDate = jdateTanggal_Setoran.getDate();
        if (utilDate == null) {
            JOptionPane.showMessageDialog(this, "Tanggal Setor tidak boleh kosong.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        java.sql.Date tanggalSetor = new java.sql.Date(utilDate.getTime());
        String nama = (String) dropdownNama.getSelectedItem();
        String jenis = (String) dropdownJenis.getSelectedItem();
        int hargaSatuan = Integer.parseInt(tfHarga_Satuan.getText());
        double kuantitas = Double.parseDouble(tfKuantitas.getText());
        int penerimaan = (int) (hargaSatuan * kuantitas);

        Setoran setoran = new Setoran(tanggalSetor, nama, jenis, hargaSatuan, kuantitas, penerimaan);
        SetoranDAO.addSetoran(setoran);

        JOptionPane.showMessageDialog(this, "Data setoran berhasil disimpan!");      
        updateTabelSetoran();


    }//GEN-LAST:event_AddSetoranButtonActionPerformed

