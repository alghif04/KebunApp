     private void loadDropdowns() {
        // Load data nama dari database
        List<String> namaList = SetoranDAO.loadNamaNasabah();
        for (String nama : namaList) {
            dropdownNama.addItem(nama);
        }

        // Load data jenis dari database
        List<String> jenisList = SetoranDAO.loadJenisHarga();
        for (String jenis : jenisList) {
            dropdownJenis.addItem(jenis);
        }
    }
     private void updateHargaSatuan() {
        String selectedJenis = (String) dropdownJenis.getSelectedItem();
        int hargaSatuan = SetoranDAO.getHargaByJenis(selectedJenis);
        tfHarga_Satuan.setText(String.valueOf(hargaSatuan));
    }

     private void updateTabelSetoran() {
    String[] columnNames = {"Tanggal Setor", "Nama", "Jenis", "Harga Satuan", "Kuantitas", "Penerimaan"};
    DefaultTableModel model = new DefaultTableModel(columnNames, 0);

    try (Connection con = DatabaseConnection.getConnection();
         PreparedStatement pst = con.prepareStatement("SELECT * FROM setoran");
         ResultSet rs = pst.executeQuery()) {
        while (rs.next()) {
            Object[] row = {
                rs.getDate("tanggal_setor"),
                rs.getString("nama"),
                rs.getString("jenis"),
                rs.getInt("harga_satuan"),
                rs.getDouble("kuantitas"),
                rs.getInt("penerimaan")
            };
            model.addRow(row);
        }
    } catch (SQLException e) {
        e.printStackTrace();
    }

    TabelSetoran.setModel(model);
}

