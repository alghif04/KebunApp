    private void TambahPanenButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_TambahPanenButtonActionPerformed
      // Get values from the UI components
    String komoditiNama = (String) dropdownKomoditi.getSelectedItem();
    String beratPesananStr = tfBeratPesanan.getText();
    Date tanggal = jDateTanggal.getDate();

    // Validate the values
    if (komoditiNama == null || beratPesananStr.isEmpty() || tanggal == null) {
        JOptionPane.showMessageDialog(this, "Please fill in all the fields.");
        return;
    }

    double beratPesanan;
    try {
        beratPesanan = Double.parseDouble(beratPesananStr);
    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "Please enter a valid number for berat.");
        return;
    }

    // Create a LaporanPanen object
    LaporanPanen laporanPanen = new LaporanPanen(0, new java.sql.Date(tanggal.getTime()), komoditiNama, beratPesanan);

    // Insert the object into the database
    LaporanPanenDAO laporanPanenDAO = new LaporanPanenDAO();
    boolean isSuccess = laporanPanenDAO.addLaporanPanen(laporanPanen);

    // Show a success or failure message to the user
    if (isSuccess) {
        JOptionPane.showMessageDialog(this, "Laporan panen berhasil ditambahkan.");
        // Optionally, you can clear the input fields or refresh the table if needed
        tfBeratPesanan.setText("");
        jDateTanggal.setDate(null);
        dropdownKomoditi.setSelectedIndex(0);
    } else {
        JOptionPane.showMessageDialog(this, "Terjadi kesalahan saat menambahkan laporan panen.");
    }
    populateTable();
    }//GEN-LAST:event_TambahPanenButtonActionPerformed

