    private void populateDropdownKomoditi() {
        List<String> komoditiNamaList = komoditiDAO.getAllKomoditiNama();

        for (String komoditiNama : komoditiNamaList) {
            dropdownKomoditi.addItem(komoditiNama);
        }
    }
    
     private void populateTable() {
        DefaultTableModel tableModel = (DefaultTableModel) TabelLaporanPanen.getModel();
        tableModel.setRowCount(0); // Clear existing rows

        LaporanPanenDAO laporanPanenDAO = new LaporanPanenDAO();
        // Fetch data from the database
        List<LaporanPanen> laporanPanenList = laporanPanenDAO.getAllLaporanPanen();

        // Populate the table with fetched data
        for (LaporanPanen laporanPanen : laporanPanenList) {
            Object[] rowData = {
                laporanPanen.getPanenTanggal(),
                laporanPanen.getPanenKomoditi(),
                laporanPanen.getPanenBerat()
            };
            tableModel.addRow(rowData);
        }
    }

