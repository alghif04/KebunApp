    private void HapusButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HapusButtonActionPerformed
   // Get the selected row index
    int selectedRowIndex = TabelLaporanPanen.getSelectedRow();

    // Check if a row is selected
    if (selectedRowIndex >= 0) {
        // Get the values of the selected row
        String tanggal = TabelLaporanPanen.getValueAt(selectedRowIndex, 0).toString(); // Assuming date is in the first column
        String komoditi = TabelLaporanPanen.getValueAt(selectedRowIndex, 1).toString(); // Assuming commodity name is in the second column
        double berat = Double.parseDouble(TabelLaporanPanen.getValueAt(selectedRowIndex, 2).toString()); // Assuming weight is in the third column

        // Remove the row from the table
        DefaultTableModel tableModel = (DefaultTableModel) TabelLaporanPanen.getModel();
        tableModel.removeRow(selectedRowIndex);

        // Remove the row from the database using the date, commodity name, and weight
        LaporanPanenDAO laporanPanenDAO = new LaporanPanenDAO();
        boolean deleted = laporanPanenDAO.deleteLaporanPanenByDetails(tanggal, komoditi, berat);
        if (deleted) {
            JOptionPane.showMessageDialog(this, "Data berhasil dihapus dari database.");
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menghapus data dari database.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus.");
    }
    }//GEN-LAST:event_HapusButtonActionPerformed

