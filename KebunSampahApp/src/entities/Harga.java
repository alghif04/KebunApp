package entities;

/**
 *
 * @author Rohan
 */
public class Harga {
    private int id;
    private String jenis;
    private double harga;
    private String satuan;

    public Harga(String jenis, double harga, String satuan) {
        this.jenis = jenis;
        this.harga = harga;
        this.satuan = satuan;
    }

    // Getters and Setters
    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getJenis() {
        return jenis;
    }

    public void setJenis(String jenis) {
        this.jenis = jenis;
    }

    public double getHarga() {
        return harga;
    }

    public void setHarga(double harga) {
        this.harga = harga;
    }

    public String getSatuan() {
        return satuan;
    }

    public void setSatuan(String satuan) {
        this.satuan = satuan;
    }
}

