private void saveKomoditiToFile(List<Komoditi> komoditiList) {
    try (ObjectOutputStream oos = new ObjectOutputStream(new FileOutputStream("TabelKomoditi.bin"))) {
        oos.writeObject(komoditiList);
    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error saving data: " + e.getMessage());
    }
}


private void populateTableFromBinaryFile() {
    try (ObjectInputStream ois = new ObjectInputStream(new FileInputStream("TabelKomoditi.bin"))) {
        List<Komoditi> komoditiList = (List<Komoditi>) ois.readObject();
        
        // Get the table model
        DefaultTableModel model = (DefaultTableModel) TabelKomoditi.getModel();
        
        // Clear existing data in the table
        model.setRowCount(0);
        
        // Populate the table with the loaded data
        for (Komoditi komoditi : komoditiList) {
            model.addRow(new Object[]{komoditi.getNama(), komoditi.getHarga(), komoditi.getSatuan()});
        }
    } catch (IOException | ClassNotFoundException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Error loading data: " + e.getMessage());
    }
}
