    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
    // Get the selected row index from the table
    int selectedRowIndex = TabelKomoditi.getSelectedRow();

    // Check if a row is selected
    if (selectedRowIndex >= 0) {
        // Get the DefaultTableModel of the table
        DefaultTableModel model = (DefaultTableModel) TabelKomoditi.getModel();

        // Remove the selected row from the table model
        model.removeRow(selectedRowIndex);

        // Save the updated table to TabelPelanggan.bin
        saveKomoditiToFile(getKomoditiListFromTableModel(model));

        JOptionPane.showMessageDialog(this, "Customer deleted successfully!");
    } else {
        JOptionPane.showMessageDialog(this, "Please select a customer to delete.");
    }
    }//GEN-LAST:event_DeleteButtonActionPerformed
private List<Komoditi> getKomoditiListFromTableModel(DefaultTableModel model) {
    List<Komoditi> komoditiList = new ArrayList<>();
    for (int i = 0; i < model.getRowCount(); i++) {
        String nama = model.getValueAt(i, 0).toString();
        double harga = Double.parseDouble(model.getValueAt(i, 1).toString());
        String satuan = model.getValueAt(i, 2).toString();
        komoditiList.add(new Komoditi(nama, harga, satuan));
    }
    return komoditiList;
}
