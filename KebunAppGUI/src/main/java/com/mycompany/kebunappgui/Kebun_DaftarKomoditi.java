    private void AddKomoditiButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddKomoditiButtonActionPerformed
    String nama = tfNama.getText();
    double harga = Double.parseDouble(tfHarga.getText());
    String satuan = (String) dropdownSatuan.getSelectedItem(); 

    // Create a new Komoditi object
    Komoditi komoditi = new Komoditi(nama, harga, satuan);

    // Add Komoditi to table model
    DefaultTableModel model = (DefaultTableModel) TabelKomoditi.getModel();
    model.addRow(new Object[]{komoditi.getNama(), komoditi.getHarga(), komoditi.getSatuan()});

    // Save updated data to file
    saveKomoditiToFile(getKomoditiListFromTableModel(model));
    }//GEN-LAST:event_AddKomoditiButtonActionPerformed

